/**
 *
 * @author pfares
 */
public class Calculette {
    public int add (int x, int y) {
        return x+y;
    }
    public int sub (int x, int y) {
        return x-y;
    }
    public Integer mult (Integer x, Integer y) {
        return x*y;
    }
    public Integer div (Integer x, Integer y) {
        return x/y;
    }
    
}
